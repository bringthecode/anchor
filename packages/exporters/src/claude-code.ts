import type { ContextSnapshot, ExportedFile } from "@bringthecode/core";
import { fileTreeToString } from "@bringthecode/core";
import type { Exporter } from "./base.js";

export class ClaudeCodeExporter implements Exporter {
  readonly name = "claude-code";
  readonly description = "Export project context for Claude Code (CLAUDE.md)";
  readonly targetFiles = ["CLAUDE.md"];

  export(snapshot: ContextSnapshot): ExportedFile[] {
    return [
      {
        path: "CLAUDE.md",
        content: this.buildClaudeMd(snapshot),
      },
    ];
  }

  private buildClaudeMd(snapshot: ContextSnapshot): string {
    const lines: string[] = [];

    lines.push(`# ${snapshot.summary.split(".")[0]}`);
    lines.push("");

    // Project overview — Claude Code benefits from a clear, upfront summary
    lines.push(`## Overview`);
    lines.push(snapshot.summary);
    lines.push("");

    // Tech stack
    const s = snapshot.stack;
    if (s.frameworks.length > 0 || s.languages.length > 0) {
      lines.push(`## Tech Stack`);
      if (s.languages.length > 0) lines.push(`- **Languages:** ${s.languages.join(", ")}`);
      if (s.frameworks.length > 0) lines.push(`- **Frameworks:** ${s.frameworks.join(", ")}`);
      if (s.buildTools.length > 0) lines.push(`- **Build tools:** ${s.buildTools.join(", ")}`);
      if (s.databases.length > 0) lines.push(`- **Databases:** ${s.databases.join(", ")}`);
      if (s.packageManager) lines.push(`- **Package manager:** ${s.packageManager}`);
      if (s.runtime) lines.push(`- **Runtime:** ${s.runtime}`);
      lines.push("");
    }

    // Project structure
    lines.push(`## Project Structure`);
    lines.push("```");
    lines.push(fileTreeToString(snapshot.fileTree));
    lines.push("```");
    lines.push("");

    // Architectural decisions — critical for Claude Code context
    const archDecisions = snapshot.decisions.filter(
      (d) => d.category === "architecture" || d.category === "technology"
    );
    if (archDecisions.length > 0) {
      lines.push(`## Architecture Decisions`);
      lines.push("");
      for (const d of archDecisions) {
        lines.push(`### ${d.title}`);
        lines.push(d.description);
        if (d.reasoning) {
          lines.push("");
          lines.push(`> Reasoning: ${d.reasoning}`);
        }
        lines.push("");
      }
    }

    // Other guidelines
    const guidelines = snapshot.decisions.filter(
      (d) => d.category !== "architecture" && d.category !== "technology"
    );
    if (guidelines.length > 0) {
      lines.push(`## Guidelines`);
      for (const d of guidelines) {
        lines.push(`- **${d.title}:** ${d.description}`);
      }
      lines.push("");
    }

    // Common commands — useful for Claude Code
    lines.push(`## Common Commands`);
    if (snapshot.stack.packageManager === "pnpm") {
      lines.push(`- Install: \`pnpm install\``);
      lines.push(`- Dev: \`pnpm dev\``);
      lines.push(`- Build: \`pnpm build\``);
      lines.push(`- Test: \`pnpm test\``);
    } else if (snapshot.stack.packageManager === "yarn") {
      lines.push(`- Install: \`yarn\``);
      lines.push(`- Dev: \`yarn dev\``);
      lines.push(`- Build: \`yarn build\``);
      lines.push(`- Test: \`yarn test\``);
    } else if (snapshot.stack.runtime === "Python") {
      lines.push(`- Install: \`pip install -r requirements.txt\``);
      lines.push(`- Run: \`python main.py\``);
    } else {
      lines.push(`- Install: \`npm install\``);
      lines.push(`- Dev: \`npm run dev\``);
      lines.push(`- Build: \`npm run build\``);
      lines.push(`- Test: \`npm test\``);
    }
    lines.push("");

    // Notes
    if (snapshot.notes.length > 0) {
      lines.push(`## Notes`);
      for (const note of snapshot.notes) {
        lines.push(`- ${note}`);
      }
      lines.push("");
    }

    // Git context
    if (snapshot.gitInfo) {
      lines.push(`## Git`);
      lines.push(`- Branch: \`${snapshot.gitInfo.branch}\``);
      lines.push(`- Last commit: ${snapshot.gitInfo.lastCommitMessage}`);
      if (snapshot.gitInfo.remoteUrl) {
        lines.push(`- Remote: ${snapshot.gitInfo.remoteUrl}`);
      }
      lines.push("");
    }

    lines.push(`<!-- Generated by Anchor · https://bringthecode.dev -->`);

    return lines.join("\n");
  }
}
